package com.eayun.eayunstack.model;

public class QosAssociation {
	private String id;
	private String association_type;
	private String name;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAssociation_type() {
		return association_type;
	}
	public void setAssociation_type(String association_type) {
		this.association_type = association_type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
